
public class Fathers{

	private int father1;
	private int father2;

	public Fathers(int pFather1, int pFather2) {
		this.father1 = pFather1;
		this.father2 = pFather2;
	}

	public int getFather1() {
		return father1;
	}

	public void setFather1(int father1) {
		this.father1 = father1;
	}

	public int getFather2() {
		return father2;
	}

	public void setFather2(int father2) {
		this.father2 = father2;
	}
	
	
}
